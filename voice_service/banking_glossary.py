def get_glossary_terms(language: str) -> dict:
    """Get banking glossary for a specific language"""
    lang_map = {
        "hi": "Hindi",
        "mr": "Marathi",
        "ta": "Tamil",
        "bn": "Bengali",
        "en": "English",
    }
    lang_name = lang_map.get(language, "English")
    return BANKING_GLOSSARY.get(lang_name, BANKING_GLOSSARY["English"])


def get_domain_forced_tokens(src_lang: str, tgt_lang: str) -> list:
    """
    Get domain-specific terms to use as forced decoding tokens
    to improve translation accuracy for banking terminology
    """
    if tgt_lang == "en":
        return list(BANKING_GLOSSARY["English"].keys())

    # Get source language terms
    src_terms = get_glossary_terms(src_lang)
    return list(src_terms.keys())


def inject_glossary_context(text: str, src_lang: str, tgt_lang: str) -> str:
    """
    Inject banking domain context into text for better translation
    """
    # Check if text contains banking keywords
    banking_keywords = ["balance", "loan", "account", "deposit", "EMI", "interest", "bank"]
    text_lower = text.lower()

    has_banking_context = any(keyword in text_lower for keyword in banking_keywords)

    if has_banking_context:
        # Add domain prefix to guide translation
        domain_prefix = "[Banking Domain] "
        return domain_prefix + text

    return text
